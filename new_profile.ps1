Function Start-Timer {
	if ($global:stopwatch -eq $null){
		$global:stopwatch = [System.Diagnostics.Stopwatch]::StartNew()
		Write-Host "Timer started."
	} elseif ($global:stopwatch.IsRunning -eq $false) {
		$global:stopwatch.Start()
		$elapsedTime = $global:stopwatch.Elapsed
		Write-Host "Timer resumed"
		return $elapsedTime
	}
	else {
		Write-Host "Timer is already running"
	}
}

Function Stop-Timer {
    if ($global:stopwatch -eq $null) {
        Write-Host "Timer has not been started yet."
        return $null
    } else {
        $global:stopwatch.Stop()
        $elapsedTime = $global:stopwatch.Elapsed
        $global:stopwatch = $null  # Reset the stopwatch for next use
        return $elapsedTime
    }
}

Function Stop-Timer-No-Reset {
    if ($global:stopwatch -eq $null) {
        Write-Host "Timer has not been started yet."
        return $null
    } else {
        $global:stopwatch.Stop()
	Write-Host "Timer stopped."
        return $null
    }
}

Function Get-Timer {
	if ($global:stopwatch -eq $null) {
		Write-Host "Timer has not been started yet."
		return $null
	}
	else {
		$elapsedTime = $global:stopwatch.Elapsed
		return $elapsedTime
	}
}

Function calculate-time {
param (
		[Parameter(Mandatory = $true)]
		[int]$seconds
	)
	$value = 0 | Select-Object -Property @{n='hours';e={[math]::Floor($seconds / 3600)}},@{n='minutes';e={[math]::Floor(($seconds % 3600) / 60)}}
	return $value
}

Function get-last-execution-time {
	param($id)
	$history = Get-History
	if($id){
		$history = (Get-History -Id $id).EndExecutionTime - (Get-History -Id $id).StartExecutionTime
	} else {
		if($history.Count -eq 0){
			$history = "No history"
		} else {
			$history = ($history)[-1].EndExecutionTime - ($history)[-1].StartExecutionTime
		}
	}
	return $history
}

Function get-time {
	$elapsedHours = $global:stopwatch.Elapsed.Hours
	$elapsedMinutes = $global:stopwatch.Elapsed.Minutes
	return "$elapsedHours h $elapsedMinutes m"
}

