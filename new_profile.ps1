Function git_commit {
	param($message, $details, $hours, $minutes)
	$TYPE=(gum choose "fix" "feat" "docs" "style" "refactor" "test" "chore" "revert")

	# Commit these changes if user confirms
	if ($hours -eq $null -and $minutes -eq $null) {
		$elapsedTime = Get-Timer
		# $time = calculate-time $elapsedTime
		$hours = $elapsedTime.hours
		$minutes = $elapsedTime.minutes
	}

	$d = (Get-Date -Format "MM/dd/yyyy dddd HH:mm K")
	$c = (Split-Path -Path (Get-Location) -Leaf)

	if($message -eq $null){
		$message= "$(gum input --value "${TYPE}:" --placeholder "Summary of this change")"
	}
	if($details -eq $null){
		$details= "$(gum write --placeholder "Details of this change")"
	}
	if($hours -eq $null -and $minutes -eq $null){
		$hours = "$(gum input --width 2 --placeholder "Hours")"
		$minutes = "$(gum input --width 2 --placeholder "Minutes")"
	}

	gum confirm "Commit changes?";
	if ($LASTEXITCODE -eq 0) {
		git add --all; git commit -m "$message" -m "$details"; git push && Add-Content -Path "D:\Local Disk D\Saif\timesheet\Entry.txt" -Value "$d | $hours | $minutes | $c | $message $details"
	} else {
		Write-Host "No changes pushed"
	}
}

